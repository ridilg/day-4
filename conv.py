
binary = '1010111'
decimal = int(binary, 2)  # Convert binary to decimal
print(decimal)  # Output will be 10

octal = '121'
decimal = int(octal, 8)  # Convert octal to decimal
print(decimal)  # Output will be 10

hexadecimal = 'F'
decimal = int(hexadecimal, 16)  # Convert hexadecimal to decimal
print(decimal)  # Output will be 10